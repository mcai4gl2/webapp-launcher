package webapp.launcher.jersey.guice.webapp;

import org.glassfish.hk2.api.ServiceLocator;
import org.glassfish.jersey.server.ResourceConfig;
import org.jvnet.hk2.guice.bridge.api.GuiceBridge;
import org.jvnet.hk2.guice.bridge.api.GuiceIntoHK2Bridge;

import javax.inject.Inject;

public class WebAppResourceConfig extends ResourceConfig {
    @Inject
    public WebAppResourceConfig(ServiceLocator serviceLocator) {
        packages("webapp.launcher.jersey.guice.webapp");

        GuiceBridge.getGuiceBridge().initializeGuiceBridge(serviceLocator);

        GuiceIntoHK2Bridge guiceBridge = serviceLocator.getService(GuiceIntoHK2Bridge.class);
        guiceBridge.bridgeGuiceInjector(SampleApplication.injector);
    }
}
